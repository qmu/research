import type { BenchmarkResult } from "./types";

const pct = (value: number): string => `${(value * 100).toFixed(1)}%`;

const ms = (value: number): string => value.toFixed(2);

const sentence = (value: string): string =>
  value.endsWith(".") ? value : `${value}.`;

export const renderRagBenchmarkReport = (result: BenchmarkResult): string => {
  const rows = result.runs
    .map(
      (run) =>
        `| ${run.backend.name} | ${run.backend.kind} | ${run.backend.embeddingCoupling} | ` +
        `${run.backend.isolatedStore ? "yes" : "no"} | ${run.provenance} | ` +
        `${pct(run.retrieval.recallAtK)} | ${pct(run.retrieval.ndcgAtK)} | ${run.retrieval.mrr.toFixed(3)} | ` +
        `${ms(run.operational.ingestMs)} | ${ms(run.operational.queryLatencyP50Ms)} | ${ms(run.operational.queryLatencyP95Ms)} | ` +
        `$${run.operational.costUsd.toFixed(4)} | ${run.backend.costNote} |`,
    )
    .join("\n");

  return `---
title: RAG backend benchmark
description: A reproducible benchmark topic comparing vector-store retrieval quality and operational behavior. This fixture report includes sqlite-vec as a local, fixed-embedding backend.
---

# RAG backend benchmark

This report records a benchmark harness for vector-store and RAG-database backends. It separates curated backend facts from measured retrieval and operational behavior. The committed report is generated by the keyless fixture path; real local sqlite-vec runs use the same dataset, embedding, scoring, and report renderer.

## Methodology

**Dataset.** The benchmark uses ${result.dataset.name} (\`${result.dataset.id}\`), a committed miniature subset shaped after SciFact. Source: ${result.dataset.source}. License note: ${sentence(result.dataset.license)} The fixture contains ${result.dataset.documents.length} documents, ${result.dataset.queries.length} queries, and ${result.dataset.qrels.length} relevance judgments.

**Metrics.** Retrieval quality is recall@${result.runs[0]?.k ?? 3}, nDCG@${result.runs[0]?.k ?? 3}, and MRR against the committed qrels. Operational measurements are ingest time, query latency p50/p95, measured scale, and estimated cost.

**Embedding boundary.** Self-managed stores use one fixed local embedding model (\`${result.runs[0]?.embeddingModel ?? "n/a"}\`) so the store comparison is isolated from embedding-provider behavior. Managed stores added by later tickets must mark \`isolatedStore: false\`.

## Results

| Backend | Kind | Embedding | Store isolated | Provenance | Recall@k | nDCG@k | MRR | Ingest ms | Query p50 ms | Query p95 ms | Cost | Cost note |
| ------- | ---- | --------- | -------------- | ---------- | -------- | ------ | --- | --------- | ------------ | ------------ | ---- | --------- |
${rows}

## Data transparency

The complete run record is committed as [\`${result.artifactPath}\`](./${result.artifactPath}). It contains the dataset rows, backend registry facts, query-level results, retrieval metrics, operational timings, provenance, and generated timestamp.

## Scope and limitations

- This foundation ticket includes sqlite-vec plus a deterministic fixture path. Cloud backends are separate follow-up tickets.
- The committed fixture report is a CI self-test. A real sqlite-vec run is local and keyless, but its timings depend on the machine.
- The miniature dataset is intended to verify the harness and metric calculations; it is not a statistical claim about backend quality at production scale.

## Reproduce

\`\`\`sh
cd packages/tech
npm install

# Keyless byte-stable fixture:
npm run rag:fixture

# Local sqlite-vec run:
npm run rag

# Estimate without writing a report:
npm run rag:estimate
\`\`\`

Generated: ${result.generatedAt}
`;
};
